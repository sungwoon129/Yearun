package kr.ac.kopo.polycms.model;

public class Hotel {
int hotelNo;
String hotelName;
String hotelPhoto;
String hotelAddress;
String hotelPhone;
String hotelInfo;
int hotelScore;
int areaNo;
public int getAreaNo() {
	return areaNo;
}
public void setAreaNo(int areaNo) {
	this.areaNo = areaNo;
}
public int getHotelNo() {
	return hotelNo;
}
public void setHotelNo(int hotelNo) {
	this.hotelNo = hotelNo;
}
public String getHotelName() {
	return hotelName;
}
public void setHotelName(String hotelName) {
	this.hotelName = hotelName;
}
public String getHotelPhoto() {
	return hotelPhoto;
}
public void setHotelPhoto(String hotelPhoto) {
	this.hotelPhoto = hotelPhoto;
}
public String getHotelAddress() {
	return hotelAddress;
}
public void setHotelAddress(String hotelAddress) {
	this.hotelAddress = hotelAddress;
}
public String getHotelPhone() {
	return hotelPhone;
}
public void setHotelPhone(String hotelPhone) {
	this.hotelPhone = hotelPhone;
}
public String getHotelInfo() {
	return hotelInfo;
}
public void setHotelInfo(String hotelInfo) {
	this.hotelInfo = hotelInfo;
}
public int getHotelScore() {
	return hotelScore;
}
public void setHotelScore(int hotelScore) {
	this.hotelScore = hotelScore;
}

}
